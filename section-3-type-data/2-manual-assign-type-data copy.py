# Categoría texto
# valor de tipo str
data = str("Max")
print(type(data))
data = str(23)
print(type(data))
data = str(56.67)
print(type(data))

# Categoría numérica
# valor de tipo int
data = int(45.45)
print(data)
print(type(data))

# valor de tipo float
data = float(44)
print(data)
print(type(data))

# valor de tipo complex
data = complex(56.98)
print(data)
print(type(data))

# Categoría secuencial
# valor de tipo list
data = list(["Macarrones", "papas", "carne"])
print(type(data))

# valor de tipo tuple
data = tuple(("Macarrones", "papas", "carne"))
print(type(data))

# valor de tipo range
data = range(8)
print(type(data))

# Categoría map
# valor de tipo dic
data = dict(uno = "Macarrones", dos = "papas", tres = "carne")
print(type(data))

# Categoría conjuntos
# valor de tipo set
data = set(("Macarrones", "papas", "carne"))
print(type(data))

# Booleans
# valor de tipo bool
data = bool(54)
print(data)
print(type(data))
data = bool(0)
print(data)
print(type(data))
data = bool("")
print(data)
print(type(data))