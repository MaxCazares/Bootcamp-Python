# Categoría texto
# valor de tipo str
data = "Max"
print(type(data))

# Categoría numérica
# valor de tipo int
data = 45
print(type(data))

# valor de tipo float
data = 45.62
print(type(data))

# valor de tipo complex
data = 56 + 56j
print(type(data))

# Categoría secuencial
# valor de tipo list
data = ["Macarrones", "papas", "carne"]
print(type(data))

# valor de tipo tuple
data = ("Macarrones", "papas", "carne")
print(type(data))

# valor de tipo range
data = range(8)
print(type(data))

# Categoría map
# valor de tipo dic
data = {1:"Macarrones", 2:"papas", 3:"carne"}
print(type(data))

# Categoría conjuntos
# valor de tipo set
data = {"Macarrones", "papas", "carne"}
print(type(data))

# Booleans
# valor de tipo bool
data = True
print(type(data))